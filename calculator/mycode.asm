ORG 100h

START:

; Show menu
MOV DX, OFFSET MENU
CALL PRINT

; Take choice
MOV AH, 01h
INT 21h
MOV BL, AL

CALL NEWLINE

CMP BL, '5'
JE END

; Input first number
MOV DX, OFFSET MSG1
CALL PRINT
CALL INPUT
MOV BH, AL

CALL NEWLINE

; Input second number
MOV DX, OFFSET MSG2
CALL PRINT
CALL INPUT
MOV CL, AL

CALL NEWLINE

; Perform operation
CMP BL, '1'
JE ADDITION

CMP BL, '2'
JE SUBTRACTION

CMP BL, '3'
JE MULTIPLICATION

CMP BL, '4'
JE DIVISION

JMP INVALID

ADDITION:
MOV AL, BH
ADD AL, CL
JMP SHOW

SUBTRACTION:
MOV AL, BH
SUB AL, CL
JMP SHOW

MULTIPLICATION:
MOV AL, BH
MUL CL
JMP SHOW

DIVISION:
CMP CL, 0
JE DIV_ZERO
MOV AL, BH
MOV AH, 0
DIV CL
JMP SHOW

DIV_ZERO:
MOV DX, OFFSET DIV_ZERO_MSG
CALL PRINT
CALL NEWLINE
JMP START

INVALID:
MOV DX, OFFSET INVALID_MSG
CALL PRINT
CALL NEWLINE
JMP START

SHOW:
CALL PRINT_NUM
CALL NEWLINE
JMP START

; -------- FUNCTIONS --------

PRINT:
MOV AH, 09h
INT 21h
RET

INPUT:
MOV AH, 01h
INT 21h
SUB AL, 30h
RET

NEWLINE:
MOV AH, 02h
MOV DL, 0DH
INT 21h
MOV DL, 0AH
INT 21h
RET

PRINT_NUM:
; Print AL as signed number, assuming -99 to 99
CMP AL, 0
JGE PRINT_POS
MOV DL, '-'
MOV AH, 02h
INT 21h
NEG AL
PRINT_POS:
MOV AH, 0
MOV BL, 10
DIV BL
CMP AL, 0
JE SKIP_TENS
ADD AL, 30h
MOV DL, AL
MOV AH, 02h
INT 21h
SKIP_TENS:
MOV AL, AH
ADD AL, 30h
MOV DL, AL
MOV AH, 02h
INT 21h
RET

END:
RET

; -------- DATA --------

MENU DB '1.Add  2.Subtract  3.Multiply  4.Divide  5.Exit $'
MSG1 DB 'Enter first number: $'
MSG2 DB 'Enter second number: $'
INVALID_MSG DB 'Invalid choice $'
DIV_ZERO_MSG DB 'Division by zero $'
